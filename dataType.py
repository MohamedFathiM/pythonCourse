# -------------------------
# type()
# All Data in Python is Object
# --------------------------


print(type(10))  # int

print(type(100.9))  # float

print(type('Hello World'))  # str

print(type(1 == 2))  # bool

print(type([1, 2, 3, 5]))  # List

print(type((1, 2, 35)))  # tuple

print(type({"one": 1, "two": 2, "three": 10}))  # dict
