# Integers
print(type(1))
print(type(-1))
print(type(0))


# Floats
print(type(1.0))
print(type(-1.0))
print(type(0.5))

# Complex
num = 5+6j
print("Real is {}".format(num.real))
print("Imaginary is {}".format(num.imag))

